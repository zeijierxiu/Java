package Thread;
/*创建多线程程序的第一种方法：创建Thread类的子类
 * java.lang.Thread类：描述线程的类，想要实现多线程就必须继承Thread类
 * 
 * 实现步骤：
 * 		1.创建Thread类的子类
 * 		2.在子类中重写Thread类中的run()方法，设置线程任务(开启线程要做什么)
 * 		3.创建Thread类的子类对象
 * 		4.调用Thread类中的start()方法，开启新线程，调用run方法
 *		void start()使该线程开始执行，java虚拟机调用该线程的run()方法 
 *	
 *		结果是两个线程并发地运行；当前线程（main线程）（从调用返回给 start 方法）和另一个线程（创建的新线程，执行其 run 方法）。 

		多次启动一个线程是非法的。特别是当线程已经结束执行后，不能再重新启动。 

 		java属于抢占式调度，优先级高的线程优先执行
 * 
 */
public class Demo1 {
	public static void main(String[] args) {
		Mythread mt = new Mythread();
		mt.start();
		
		for (int i = 0; i < 10; i++) {
			System.out.println("main : "+i );
		}
	}
	
}
